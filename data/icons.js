(function applyWuwaIconAssets() {
  const assets = window.WUWA_ICON_ASSETS || {};
  const chars = window.WUWA && window.WUWA.chars ? window.WUWA.chars : {};
  Object.entries(assets.characters || {}).forEach(([id, icon]) => {
    if (chars[id]) chars[id].portrait = icon;
  });
  const weapons = (window.WUWA_DATA && window.WUWA_DATA.weapons) || [];
  Object.entries(assets.weapons || {}).forEach(([id, icon]) => {
    const weapon = weapons.find((item) => item.id === id || item.name === id);
    if (weapon) weapon.icon = icon;
  });
  const sonatas = window.WUWA_SONATAS || [];
  Object.entries(assets.sonatas || {}).forEach(([id, icon]) => {
    const set = sonatas.find((item) => String(item.id) === id || item.name === id);
    if (set) set.icon = icon;
  });
})();
